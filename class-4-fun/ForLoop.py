# For loop in List
enames = ["SG","RG","SG","RG"]
for ename in enames:
    print(ename)


# For loop in tuple
enames = ("SG","RG","SG","RG")
for ename in enames:
    print(ename)

#For loop in Set

enames = {"SG","RG","SG","RG"}
for ename in enames:
    print(ename)


# For loop in String

enames = 'Rahul'
for ename in enames:
    print(ename)


# For loop in Bytes

enames = ["SG","RG","SG","RG"]
for ename in enames:
    print(ename)