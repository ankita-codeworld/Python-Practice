#Example of a break, continue keywords and identifier.

# execute for loop
for i in range(1, 11):
    
    # print the value of i
    print(i)
    
    # check the value of i is less than 5
    # if i lessthan 5 then continue loop
    if i < 5:  
        continue
        
    # if i greater than 5 then break loop
    else:  
        break




